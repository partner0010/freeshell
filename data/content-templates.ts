export function searchTemplates(filters: {
  category?: string;
  contentType?: string;
  platform?: string;
  tags?: string[];
  isPremium?: boolean;
  searchQuery?: string;
}): ContentTemplate[] {
  return contentTemplates.filter(template => {
    if (filters.category && template.category !== filters.category) return false;
    if (filters.contentType && template.contentType !== filters.contentType) return false;
    if (filters.platform && !template.platform.includes(filters.platform)) return false;
    if (filters.isPremium !== undefined && template.isPremium !== filters.isPremium) return false;
    if (filters.searchQuery) {
      const query = filters.searchQuery.toLowerCase();
      const matchesTitle = template.title.toLowerCase().includes(query);
      const matchesDescription = template.description.toLowerCase().includes(query);
      const matchesTags = template.tags.some(tag => tag.toLowerCase().includes(query));
      if (!matchesTitle && !matchesDescription && !matchesTags) return false;
    }
    if (filters.tags && filters.tags.length > 0) {
      const hasMatchingTag = filters.tags.some(tag => 
        template.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
      );
      if (!hasMatchingTag) return false;
    }
    return true;
  });
}

export function getTemplateById(id: string): ContentTemplate | undefined {
  return contentTemplates.find(t => t.id === id);
}

export function getPopularTemplates(limit: number = 10): ContentTemplate[] {
  return [...contentTemplates]
    .sort((a, b) => (b.usageCount || 0) - (a.usageCount || 0))
    .slice(0, limit);
}

export function getTemplatesByCategory(category: string): ContentTemplate[] {
  return contentTemplates.filter(t => t.category === category);
}

